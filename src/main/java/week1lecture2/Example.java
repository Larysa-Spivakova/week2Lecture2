package week1lecture2;

public class Example {
    public static void main(String[] args) {
        int n, d;

        n = 10;
        d = 2;
        if (d != 0 && (n % d) == 0)
            System.out.println(d + " delitel for " + n);

        d = 0;
        if (d != 0 && (n % d) == 0)
            System.out.println(d + " delitel for " + n);

        if (d != 0 & (n % d) == 0)
            System.out.println(d + " delitel for " + n);

    }
}
